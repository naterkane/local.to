        <h3>What Does TeamItUp Do?</h3>
        <p>
            TeamItUp is a sports tool that makes the communication, management, and promotion of sports teams and its members more efficient.
        </p>
        <h3>How Does Teamitup Work?</h3>
        <p>
            Teamitup allows users to join their team networks and share information in a timely manner. All users and teams can choose how private they would like to be.
        </p>
        <h3>Why Use Teamitup?</h3>
        <p>
            TeamItUp is a free service that levels the playing field and gives participants at every level of sport equal resources to communicate, manage, and promote their sports lifestyle. We created it to help any team (and its members) enhance their success on and off the field.
        </p>
        <h3>Who Can Use TeamItUp?</h3>
        <p>
            Anyone on a team can start their own TeamItUp network and invite other teammates to sign up and join their network. There are no limits as to how many teams a member can join.
        </p>
        <h3>Who Is TeamItUp?</h3>
        <p>
            TeamItUp was created by Perry Interactive Media, Inc., a privately-held New York corporation led by a focused, dedicated team of experienced sports executives. Each member has a background in the online (sports) marketplace and a passion for bringing sports-focused information and profiles to global audiences in an efficient and effective way. Our passion comes from a lifetime of playing sports, most notably at the collegiate and professional levels.
        </p>
